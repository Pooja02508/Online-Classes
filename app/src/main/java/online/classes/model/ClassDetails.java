package online.classes.model;

public class ClassDetails {

    String subject,state,startedDate,endDate,address,teacherName,district,language,whatsappLink;

    public ClassDetails() {

    }

    public ClassDetails(String subject, String state,String district, String startedDate, String endDate, String address, String teacherName,String language,String whatsappLink) {
        this.subject = subject;
        this.state = state;
        this.district=district;
        this.startedDate = startedDate;
        this.endDate = endDate;
        this.address = address;
        this.teacherName = teacherName;
        this.language=language;
    }

    public String getSubject() {
        return subject;
    }

    public void setSubject(String subject) {
        this.subject = subject;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getStartedDate() {
        return startedDate;
    }

    public void setStartedDate(String startedDate) {
        this.startedDate = startedDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }

    public String getLanguage() {
        return language;
    }

    public void setLanguage(String language) {
        this.language = language;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTeacherName() {
        return teacherName;
    }

    public void setTeacherName(String teacherName) {
        this.teacherName = teacherName;
    }

    public String getWhatsappLink() {
        return whatsappLink;
    }

    public void setWhatsappLink(String whatsappLink) {
        this.whatsappLink = whatsappLink;
    }
}
